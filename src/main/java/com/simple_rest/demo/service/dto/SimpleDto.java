package com.simple_rest.demo.service.dto;

public class SimpleDto {
    private int id;

    private String name;

    public SimpleDto() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
